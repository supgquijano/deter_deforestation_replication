Z95 = norm.ppf(0.975)   # 1.95996
Z99 = norm.ppf(0.995)   # 2.57583


def format_spec(s):
    adj05 = tf_adj(s["F"], 0.05)
    adj01 = tf_adj(s["F"], 0.01)

    se_tf05 = s["se"] * adj05
    se_tf01 = s["se"] * adj01

    t_conv = s["beta"] / s["se"]
    t_tf05 = s["beta"] / se_tf05
    t_tf01 = s["beta"] / se_tf01

    ci95_conv = (s["beta"] - Z95 * s["se"], s["beta"] + Z95 * s["se"])
    ci95_tf   = (s["beta"] - Z95 * se_tf05, s["beta"] + Z95 * se_tf05)
    ci99_conv = (s["beta"] - Z99 * s["se"], s["beta"] + Z99 * s["se"])
    ci99_tf   = (s["beta"] - Z99 * se_tf01, s["beta"] + Z99 * se_tf01)

    sig_conv_5  = abs(t_conv) >  Z95
    sig_tf_5    = abs(t_tf05) >  Z95
    sig_conv_1  = abs(t_conv) >  Z99
    sig_tf_1    = abs(t_tf01) >  Z99

    print(f"--- {s['tag']} ---")
    print(f"  β = {s['beta']:+.4f}   SE conv = {s['se']:.4f}   F = {s['F']:.3f}")
    print(f"  adj_5%  = {adj05:.4f}   adj_1%  = {adj01:.4f}")
    print()
    print(f"  {'':22} {'SE':>8} {'t':>7} {'CI low':>9} {'CI high':>9}  sig?")
    print(f"  {'Conventional 5%':22} {s['se']:>8.4f} {t_conv:>7.3f} "
          f"{ci95_conv[0]:>9.4f} {ci95_conv[1]:>9.4f}  {'YES' if sig_conv_5 else 'no '}")
    print(f"  {'tF-adjusted   5%':22} {se_tf05:>8.4f} {t_tf05:>7.3f} "
          f"{ci95_tf[0]:>9.4f} {ci95_tf[1]:>9.4f}  {'YES' if sig_tf_5 else 'no '}")
    print(f"  {'Conventional 1%':22} {s['se']:>8.4f} {t_conv:>7.3f} "
          f"{ci99_conv[0]:>9.4f} {ci99_conv[1]:>9.4f}  {'YES' if sig_conv_1 else 'no '}")
    print(f"  {'tF-adjusted   1%':22} {se_tf01:>8.4f} {t_tf01:>7.3f} "
          f"{ci99_tf[0]:>9.4f} {ci99_tf[1]:>9.4f}  {'YES' if sig_tf_1 else 'no '}")
    print(f"  CI length increase (5%):  {(ci95_tf[1]-ci95_tf[0])/(ci95_conv[1]-ci95_conv[0]):.2f}x")
    print(f"  CI length increase (1%):  {(ci99_tf[1]-ci99_tf[0])/(ci99_conv[1]-ci99_conv[0]):.2f}x")
    print()


