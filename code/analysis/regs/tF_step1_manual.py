def tf_adj(F, level=0.05):
    """Factor de ajuste tF interpolado linealmente."""
    table = TBL_3A if level == 0.05 else TBL_3B
    Fs = [row[0] for row in table]
    adjs = [row[1] for row in table]
    if F <= Fs[0]:
        return float('inf')
    if F >= Fs[-1]:
        return 1.0 if level == 0.05 else adjs[-1]
    return float(np.interp(F, Fs, adjs))


